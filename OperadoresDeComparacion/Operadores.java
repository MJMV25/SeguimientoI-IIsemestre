package OperadoresDeComparacion;

public class Operadores {
    int a = 5;
    int b = 3;

    // Igual a
    boolean igual = (a == b);  // false, porque 5 no es igual a 3

    // No igual a
    boolean noIgual = (a != b);  // true, porque 5 no es igual a 3

    // Mayor que
    boolean mayorQue = (a > b);  // true, porque 5 es mayor que 3
}
